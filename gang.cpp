#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

// ??????????????????????????????????????????????
int countCaught(vector<char>& row, int k) {
    int caught = 0;
    vector<bool> caughtBy(row.size(), false);  // ?????????? P ??????????? T ?????????
    for (int i = 0; i < row.size(); ++i) {
        if (row[i] == 'P') {  // ????????????????
            for (int j = max(0, i-k); j <= min(int(row.size()-1), i+k); ++j) {  // ????????????????????????????
                if (row[j] == 'T' && !caughtBy[j]) {  // ???????????? T ???????????????
                    caught++;
                    caughtBy[j] = true;  // ????????????????????????????????
                    break;
                }
            }
        }
    }
    return caught;
}

int main() {
    int N, k;
    cin >> N >> k;
    vector<vector<char>> grid(N, vector<char>(N));  // ????? N×N

    for (int i = 0; i < N; ++i) {
        for (int j = 0; j < N; ++j) {
            cin >> grid[i][j];
        }
    }

    int totalCaught = 0;
    // ?????????????????
    for (int i = 0; i < N; ++i) {
        totalCaught += countCaught(grid[i], k);
    }
    
    cout << totalCaught << endl;

    return 0;
}

